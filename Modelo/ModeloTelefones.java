
package Modelo;





public class ModeloTelefones {
    private int cod_tel;
    private String num_tel;

    /**
     * @return the cod_tel
     */
    public int getCod_tel() {
        return cod_tel;
    }

    /**
     * @param cod_tel the cod_tel to set
     */
    public void setCod_tel(int cod_tel) {
        this.cod_tel = cod_tel;
    }

    /**
     * @return the num_tel
     */
    public String getNum_tel() {
        return num_tel;
    }

    /**
     * @param num_tel the num_tel to set
     */
    public void setNum_tel(String num_tel) {
        this.num_tel = num_tel;
    }
    
}
