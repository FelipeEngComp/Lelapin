package Modelo;


public class ModeloFornecedores {
    private int cod_forne;
    private String nome_forne;
    private String end_forne;
    private int cod_cid_forne;
    private int cod_bairro_forne;
    private String cnpj_forne;
    private int cod_tel_forne;

    /**
     * @return the cod_forne
     */
    public int getCod_forne() {
        return cod_forne;
    }

    /**
     * @param cod_forne the cod_forne to set
     */
    public void setCod_forne(int cod_forne) {
        this.cod_forne = cod_forne;
    }

    /**
     * @return the nome_forne
     */
    public String getNome_forne() {
        return nome_forne;
    }

    /**
     * @param nome_forne the nome_forne to set
     */
    public void setNome_forne(String nome_forne) {
        this.nome_forne = nome_forne;
    }

    /**
     * @return the end_forne
     */
    public String getEnd_forne() {
        return end_forne;
    }

    /**
     * @param end_forne the end_forne to set
     */
    public void setEnd_forne(String end_forne) {
        this.end_forne = end_forne;
    }

    /**
     * @return the cod_cid_forne
     */
    public int getCod_cid_forne() {
        return cod_cid_forne;
    }

    /**
     * @param cod_cid_forne the cod_cid_forne to set
     */
    public void setCod_cid_forne(int cod_cid_forne) {
        this.cod_cid_forne = cod_cid_forne;
    }

    /**
     * @return the cod_bairro_forne
     */
    public int getCod_bairro_forne() {
        return cod_bairro_forne;
    }

    /**
     * @param cod_bairro_forne the cod_bairro_forne to set
     */
    public void setCod_bairro_forne(int cod_bairro_forne) {
        this.cod_bairro_forne = cod_bairro_forne;
    }

    /**
     * @return the cnpj_forne
     */
    public String getCnpj_forne() {
        return cnpj_forne;
    }

    /**
     * @param cnpj_forne the cnpj_forne to set
     */
    public void setCnpj_forne(String cnpj_forne) {
        this.cnpj_forne = cnpj_forne;
    }

    /**
     * @return the cod_tel_forne
     */
    public int getCod_tel_forne() {
        return cod_tel_forne;
    }

    /**
     * @param cod_tel_forne the cod_tel_forne to set
     */
    public void setCod_tel_forne(int cod_tel_forne) {
        this.cod_tel_forne = cod_tel_forne;
    }

    
}
