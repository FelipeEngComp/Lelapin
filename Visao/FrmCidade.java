    public void AtualizaTabelaEstado(){
        conEstado.conexao();
      
        jComboBoxEstado.removeAllItems();//limpa a combo box        
        try{
            conEstado.executaSQL("select * from estados order by nome_estado");//seleciona a tabela estado e ordena por ordem alfabetica
            conEstado.rs.first();
            do{
                jComboBoxEstado.addItem(conEstado.rs.getString("nome_estado"));
            }while(conEstado.rs.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Erro ao conectar ao banco de dados estado.\nErro: "+ex);
        }
        conEstado.desconecta();
        
    }
    
    
    public void PreencherTabela(String SQL){
        conCidade.conexao();
        
        ArrayList dados = new ArrayList();
        
        String [] colunas = new String[]{"ID","Cidade","Estado"};
        
        conCidade.executaSQL(SQL);
        try{
            conCidade.rs.first();
            do{
                dados.add(new Object[]{conCidade.rs.getInt("id_cidade"),conCidade.rs.getString("nome_cidades"),conCidade.rs.getString("sigla_estado")});
            }while(conCidade.rs.next());
        }    
        catch(SQLException ex){
             JOptionPane.showMessageDialog(null,"Erro ao inserir na tabela");
        }
        
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableCidade.setModel(modelo);
        jTableCidade.getColumnModel().getColumn(0).setPreferredWidth(109);//definindo tamanho em pixel
        jTableCidade.getColumnModel().getColumn(0).setResizable(false);
        jTableCidade.getColumnModel().getColumn(1).setPreferredWidth(300);//definindo tamanho em pixel
        jTableCidade.getColumnModel().getColumn(1).setResizable(false);
        jTableCidade.getColumnModel().getColumn(2).setPreferredWidth(109);//definindo tamanho em pixel
        jTableCidade.getColumnModel().getColumn(2).setResizable(false);
        //chama a tabela
        jTableCidade.getTableHeader().setReorderingAllowed(false);//nao vai poder reordenar alocacao
        jTableCidade.setAutoResizeMode(jTableCidade.AUTO_RESIZE_OFF);//defino se vou poder mexer como um todo 
        jTableCidade.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//poder selecionar apenas um elemento da tabela
        
        conCidade.desconecta();
        
    }
