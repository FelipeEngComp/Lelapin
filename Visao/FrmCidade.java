    private void jButtonPrimeiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPrimeiroActionPerformed
        // TODO add your handling code here:
        conEstado.conexao();
        conCidade.conexao();
        jTextFieldNome.setEnabled(true);
        jButtonAtualizar.setEnabled(true);
        jButtonDeletar.setEnabled(true);
        
        conCidade.executaSQL("select * from cidades order by id_cidade");
        try {
            conCidade.rs.first();
            jTextFieldCod.setText(String.valueOf(conCidade.rs.getInt("id_cidade")));
            jTextFieldNome.setText(conCidade.rs.getString("nome_cidades"));
            
            conEstado.executaSQL("select * from estados where id_estados = '"+conCidade.rs.getInt("id_estado")+"'");
            conEstado.rs.first();
            jComboBoxEstado.setSelectedItem(conEstado.rs.getString("nome_estado"));
        } catch (SQLException ex) {
            Logger.getLogger(FrmCidade.class.getName()).log(Level.SEVERE, null, ex);
        }
        conEstado.desconecta();
        conCidade.desconecta();
    }//GEN-LAST:event_jButtonPrimeiroActionPerformed

    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        conEstado.conexao();
        try {
            // TODO add your handling code here:
            
            mod.setNome(jTextFieldNome.getText().toUpperCase());
            conEstado.executaSQL("select * from estados where nome_estado = '"+jComboBoxEstado.getSelectedItem()+"'");//selecionar o estado
            conEstado.rs.first();
           
            mod.setCod_estado(conEstado.rs.getInt("id_estados"));
            control.InserirCidade(mod);
            
           
            
        } catch (SQLException ex) {
            Logger.getLogger(FrmCidade.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        jTextFieldNome.setEnabled(false);
        jButtonAtualizar.setEnabled(false);
        jButtonDeletar.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jTextFieldNome.setText("");
        jTextFieldCod.setText("");
        jButtonCancelar.setEnabled(false);
        jButtonNovo.setEnabled(true);
        
        PreencherTabela("select * from cidades inner join estados on id_estado=id_estados order by id_cidade");
        conEstado.desconecta();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

